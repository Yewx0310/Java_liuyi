package day4;

public class ForDemo2 {
    public static void main(String[] args) {
        for(int i = 1;i<=10;i++){
            System.out.println(i);
        }

        for(int j = 0;j<10;j++){
            System.out.println(j+1);
        }

        for(int k = 10;k>=1;k--){
            System.out.println(k);
        }
    }
}
